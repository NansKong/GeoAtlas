import pandas as pd
import xgboost as xgb
import os
import joblib
import logging
from typing import Dict, Any

logger = logging.getLogger(__name__)

class TrendForcePredictor:
    """
    XGBoost tabular model meant for 24h - 7d prediction horizons.
    Uses dataset generated by dataset_builder.py.
    """
    def __init__(self, model_path: str = "data/ml/trendforce.json"):
        self.model_path = model_path
        self.model = None
        self._load_model()
        
    def _load_model(self):
        if os.path.exists(self.model_path):
            try:
                self.model = xgb.XGBClassifier()
                self.model.load_model(self.model_path)
            except Exception as e:
                logger.error(f"Failed to load XGBoost model: {e}")
                
    def train(self, dataset_path: str):
        if not os.path.exists(dataset_path):
            logger.error("Dataset not found for training TrendForce.")
            return

        df = pd.read_csv(dataset_path)
        
        # Super simple feature engineering
        # Convert categoricals to dummy variables
        features = ["event_type", "sentiment_score", "severity"]
        df = df.dropna(subset=["sentiment_score", "severity", "label_24h"])
        
        X = pd.get_dummies(df[features])
        # Map labels to ints: negative: 0, neutral: 1, positive: 2
        label_map = {"negative": 0, "neutral": 1, "positive": 2}
        y = df["label_24h"].map(label_map)
        
        self.model = xgb.XGBClassifier(
            n_estimators=100, 
            learning_rate=0.1, 
            max_depth=5, 
            random_state=42,
            tree_method="hist", # Compatible with fast cpu/gpu inference
            device="cuda" # We specify CUDA per user prompt enabled CUDA
        )
        
        try:
            self.model.fit(X, y)
            os.makedirs(os.path.dirname(self.model_path), exist_ok=True)
            self.model.save_model(self.model_path)
            logger.info("TrendForce models trained successfully.")
            
            # Save the training columns to handle padding on inference
            joblib.dump(X.columns.tolist(), self.model_path + ".cols")
        except Exception as e:
            logger.error(f"XGBoost training failed on CUDA. Error: {e}. Ensure GPU is reachable.")

    def predict(self, feature_dict: Dict[str, Any]) -> dict:
        if not self.model:
            return {"direction": "neutral", "confidence": 0.0, "horizon": "24h"}
            
        try:
            cols = joblib.load(self.model_path + ".cols")
        except:
            return {"direction": "neutral", "confidence": 0.0, "horizon": "24h"}

        # Build one-row dataframe
        df = pd.DataFrame([feature_dict])
        df = pd.get_dummies(df)
        
        # Re-align columns to match training set
        for c in cols:
            if c not in df.columns:
                df[c] = 0
        df = df[cols]

        pred_idx = self.model.predict(df)[0]
        probs = self.model.predict_proba(df)[0]
        
        reverse_map = {0: "down", 1: "neutral", 2: "up"}
        direction = reverse_map.get(pred_idx, "neutral")
        
        return {
            "direction": direction,
            "confidence": float(max(probs)),
            "horizon": "24h",
            "model_version": "TrendForce-XGB-v1"
        }
